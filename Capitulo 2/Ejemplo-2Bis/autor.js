class Autor {
  constructor(name, dateAge, nationatily) {
    this.name = name
    this.dateAge = dateAge
    this.nationatily = nationatily
  }
  getName() {
    return this.name
  }
  setName(date) {
    this.name = date
  }
  getDateAge() {
    return this.dateAge
  }
  setDateAge(date) {
    this.dateAge = date
  }
  getNationality() {
    return this.nationatily
  }
  setNationality(date) {
    this.nationatily = date
  }
}
