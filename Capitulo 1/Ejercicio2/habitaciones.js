class Habitaciones {
  constructor(planta, cantCamas, tel) {
    this.planta = planta
    this.cantCamas = cantCamas
    this.tel = tel
  }
  getPlanta() {
    return this.planta
  }
  setPlanta(date) {
    this.planta = date
  }
  getCantCamas() {
    return this.cantCamas
  }
  setCantCamas(date) {
    this.cantCamas = date
  }
  getTel() {
    return this.tel
  }
  setTel(date) {
    this.tel = date
  }
}
