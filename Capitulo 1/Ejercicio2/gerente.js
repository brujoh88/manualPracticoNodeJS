class Gerente {
  constructor(name, dni, tel) {
    this.name = name
    this.dni = dni
    this.tel = tel
  }
  getName() {
    return this.name
  }
  setName(date) {
    this.name = date
  }
  getDni() {
    return this.dni
  }
  setDni(date) {
    this.dni = date
  }
  getTel() {
    return this.tel
  }
  setTel(date) {
    this.tel = date
  }
}
